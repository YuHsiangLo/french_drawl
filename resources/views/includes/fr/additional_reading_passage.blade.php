<p>
    Dans ce dernier enregistrement, nous aimerions vous entendre parler de <strong>la langue française dans votre quotidien et son importance pour vous</strong>. Ce n'est pas un sujet facile à aborder, nous le savons bien! Mais nous vous donnerons quelques questions et pistes de réflexion pour vous aider.
</p>
<p>
    Dans cet enregistrement, ne révélez pas d'informations personnelles telles que votre nom, adresse ou lieu de travail car celles-ci devront être effacées de votre enregistrement pour maintenir votre confidentialité. Mais vous pouvez mentionner l'endroit où vous habitez, si vous le voulez.
</p>
<p>
    Votre enregistrement devrait durer entre 30 secondes et 2 minutes.
</p>
<p>
    Quand vous êtes prêt.e, cliquez sur l'icône du micro et commencez l'enregistrement. Une fois terminé, cliquez à nouveau sur le micro pour terminer l'enregistrement, puis envoyez-le-nous en cliquant sur l'icône de la boîte avec une flèche!
</p>
<p>
    Voici quelques questions pour vous aider à faire cet enregistrement :
</p>
<ul>
    <li>Est-ce que vous parlez souvent français dans votre quotidien?</li>
    <li>Est-ce que les personnes dans votre entourage parlent français?</li>
    <li>Est-ce que vous êtes dans un contexte où le français est en contact avec une autre langue? Ou plusieurs autres langues?</li>
    <li>Est-ce que vous habitez dans une province majoritairement anglophone? Ou dans un quartier multiculturel et multilingue?</li>
    <li>Si vous avez des enfants, est-ce que vous considérez important de leur parler en français? Ou si vous n'avez pas d'enfants mais prévoyez en avoir, est-ce que vous leur parlerez en français? Pourquoi?</li>
    <li>Est-ce que vous considérez que le maintien de la langue française au Canada est important?</li>
    <li>Pourquoi est-ce que vous aimez la langue française?</li>
    <li>Pourquoi est-ce que vous trouvez que la langue française est belle?</li>
    <li>Quel est votre attachement à la langue française?</li>
    <li>Qu'est-ce que la langue française représente pour vous?</li>
</ul>
<p>
    <strong>Pas besoin de répondre à toutes ces questions! Le but est seulement de vous inspirer!</strong>
</p>
<p>
    Une fois l'enregistrement envoyé, c'est terminé!
</p>
