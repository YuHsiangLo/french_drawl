<p>
    Gédéon se leva. Il s'approcha. Il prit la bougie, l'éleva. Entre les mains du jeune homme, au déplacement d'air, bougea un morceau de papier.
</p>
<p>
    — Hé, hé, fit Gédéon, un beau dessin !
</p>
<p>
    C'était la tête d'un vieux à la lippe un peu triste, aux yeux égarés en des souvenirs brouillés, avec des joues couvertes d'un poil rêche et des cheveux grisâtres lui retombant en paquets sur les oreilles et sur le front. Gédéon ne se reconnaissait pas encore tout à fait. Puis il eut un grand sursaut. Il regarda mieux, et, doucement, se mit à pleurer. Ce n'était pas de chagrin. Ah, Seigneur, c'eût été plutôt de délivrance !
</p>
<p>
    — C'est moi, se prit-il à gémir comme s'il y avait là du bonheur. C'est bien moi, va !
</p>
<p>
    Il n'en revenait pas. Il y avait là un sortilège. Un étranger était survenu, sans bagage, pour ainsi dire sans nom, Pierre, c'est tout. Il s'était assis un moment, avait écouté Gédéon, prenant quelques notes ; et, à présent, tout était sur un bout de papier grand comme la main : la vie, bizarre, un jour après un autre, et cela fait le temps, le passé ; cela vous conduit à la mort. Hier on était jeune ; aujourd'hui on est vieux. Tout y était : l'étonnement d'avoir vécu, l'âge, qu'on en fût rendu là à force de patienter, et jusqu'à la douce tristesse que cela fût enfin compris. Gédéon pleura quelques minutes encore sur cet étrange chagrin merveilleux. Il renifla un bon coup, s'essuyant le nez et la bouche du revers de sa main poilue. Puis il pressa Pierre de s'installer ici comme chez lui, d'y rester tout le temps qu'il lui plairait ; pourquoi pas tout l'été ?
</p>
