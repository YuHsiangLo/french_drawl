<h4 style="text-align: center;">ATLAS SONORE DES VARIÉTÉS DE FRANÇAIS DU CANADA</h4>
<p>
    Merci pour l'intérêt que vous portez à notre étude sur la variation linguistique à travers le Canada. Ce projet vise à valoriser les différents accents de la francophonie canadienne et à les situer géographiquement.
</p>
<p>
    Nous vous invitons à participer à notre collecte de données en nous envoyant des enregistrements vocaux. Ces enregistrements seront ensuite placés sur une carte du Canada. Les gens pourront ainsi voyager avec leur curseur d'un coin à l'autre du pays en écoutant différents accents.
</p>
<p>
    Votre participation à cette étude est divisée en trois parties. Tout d'abord, vous répondrez à quelques questions vous concernant (âge, lieu d'origine et langues parlées, par exemple). Vous devrez ensuite lire un court passage en français (pas plus de deux minutes !). Puis vous terminerez en enregistrant un court message d'environ une ou deux minutes où vous nous dites ce que représente la langue française pour vous, dans votre quotidien.
</p>
<p>
    N'hésitez pas à nous écrire si vous avez des questions. Vous pouvez nous contacter à l'adresse courriel suivante : <a href="mailto:me.bouchard@ubc.ca">me.bouchard@ubc.ca</a>
</p>
<p>
    Votre participation est importante à la réalisation de ce projet. Nous vous remercions sincèrement pour votre temps !
</p>
